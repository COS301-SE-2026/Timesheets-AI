package timesheets.enums;

//This enum represents the different statuses that a project can have in the timesheet application.
//ACTIVE: The project is currently active and work is being done on it.
//ARCHIVED: The project is no longer active and has been archived for record-keeping purposes.
//ON_HOLD: The project is temporarily paused and work is not currently being done on it
public enum ProjectStatus {
    ACTIVE,
    ARCHIVED,
    ON_HOLD
}